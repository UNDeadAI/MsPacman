package pacman;


public class Pair {
    public int l;
    public int r;

    public Pair(int l, int r){
        this.l = l;
        this.r = r;
    }

    public int getL() {
        return l;
    }

    public int getR() {
        return r;
    }

    public void setL(int l) {
        this.l = l;
    }

    public void setR(int r) {
        this.r = r;
    }
}
